package loci.common.utests;

import java.io.IOException;

import loci.common.HandleException;
import loci.common.IRandomAccess;
import loci.common.utests.providers.IRandomAccessProvider;
import loci.common.utests.providers.IRandomAccessProviderFactory;

import org.testng.annotations.BeforeMethod;
import org.testng.annotations.Parameters;
import org.testng.annotations.Test;

/**
 * Tests for ensuring that read-only IRandomAccess objects throw an
 * appropriate exception when a write method is called.
 *
 * <dl><dt><b>Source code:</b></dt>
 * <dd><a href="http://dev.loci.wisc.edu/trac/java/browser/trunk/components/common/test/loci/common/utests/ReadOnlyTest.java">Trac</a>,
 * <a href="http://dev.loci.wisc.edu/svn/java/trunk/components/common/test/loci/common/utests/ReadOnlyTest.java">SVN</a></dd></dl>
 *
 * @see loci.common.IRandomAccess
 */
public class ReadOnlyTest {

  private static final byte[] PAGE = new byte[] { };

  private static final String MODE = "r";

  private static final int BUFFER_SIZE = 1024;

  private IRandomAccess fileHandle;

  @Parameters({"provider"})
  @BeforeMethod
  public void setUp(String provider) throws IOException {
    IRandomAccessProviderFactory factory = new IRandomAccessProviderFactory();
    IRandomAccessProvider instance = factory.getInstance(provider);
    fileHandle = instance.createMock(PAGE, MODE, BUFFER_SIZE);
  }

  @Test(expectedExceptions={HandleException.class})
  public void isReadOnly() throws IOException {
    fileHandle.write(0);
  }

}
